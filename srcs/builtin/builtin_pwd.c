#include "../../includes/minishell.h"

int	ft_pwd(void)
{
	char	*cwd;

	cwd = getcwd(NULL, 0);
	if (!cwd)
	{
		perror("minishell: pwd");
		g_exit_code = 1;
		return (1);
	}
	printf("%s\n", cwd);
	free(cwd);
	return (0);
}

